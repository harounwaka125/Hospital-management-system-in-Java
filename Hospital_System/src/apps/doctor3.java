    private void btn_idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btn_idKeyReleased
        String id = btn_id.getText();

        try {
            PreparedStatement stmt = con.prepareStatement("select * from patient where p_id=?");

            stmt.setString(1, id);
            ResultSet rs = stmt.executeQuery();
            while (rs.next()) {
                if (id.equals(rs.getString("p_id"))) {
                    btn_name.setText(rs.getString("p_f_name"));
                    btn_phon.setText(rs.getString("p_phoneNum"));
                    btn_age.setSelectedItem(rs.getString("p_age"));
                    btn_blood.setSelectedItem(rs.getString("p_bloodType"));
                    String st = rs.getString("p_gender");
                    if (st.equals("male")) {
                        btn_male.setSelected(true);
                    } else {
                        btn_female.setSelected(true);
                    }

                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(NurseForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_idKeyReleased

