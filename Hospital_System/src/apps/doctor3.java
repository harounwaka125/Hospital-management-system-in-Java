    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here
        if (btn_name.getText().equals("") || btn_med.getText().equals("") || btn_not.getText().equals("") || btn_phon.getText().isEmpty() || btn_gen.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "please ");
        } else {
            try {

                int id = Integer.parseInt(btn_id.getText());
                String medicine = btn_med.getText();
                String note = btn_not.getText();
                String syp = btn_syp.getText();
                String gen = btn_gen.getText();
                PreparedStatement stmt = con.prepareStatement("update patient set p_med=?,p_note=?,p_syp=?,p_gen=? where p_id=?");
                stmt.setString(1, medicine);
                stmt.setString(2, note);
                stmt.setString(3, syp);
                stmt.setString(4, gen);
                stmt.setInt(5, id);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "save success");
                btn_age.setSelectedIndex(0);
                btn_blood.setSelectedIndex(0);
                btn_gen.setText("");
                btn_id.setText("");
                btn_med.setText("");
                btn_name.setText("");
                btn_not.setText("");
                btn_phon.setText("");
                btn_syp.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(doctor3.class.getName()).log(Level.SEVERE, null, ex);
            }

    }//GEN-LAST:event_jButton1ActionPerformed
    }
