                PreparedStatement stmt = con.prepareStatement("delete from doctor where doctor_id=?");
                stmt.setInt(1, (int) doctor_table.getValueAt(doctor_table.getSelectedRow(), 0));
                stmt.executeUpdate();
//            dtm.removeRow(jTable1.getSelectedRow());

                DTM.removeRow(doctor_table.getSelectedRow());
