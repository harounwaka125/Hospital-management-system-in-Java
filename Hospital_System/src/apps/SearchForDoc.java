    public void ShowDataInTable() {
        try {
            PreparedStatement stmt = con.prepareStatement("select *from doctor");
            ResultSet rs = stmt.executeQuery();
            while (rs.next()) {

                int id = rs.getInt("doctor_id");
                String password = rs.getString("d_password");
                String username = rs.getString("d_username");
                String name = rs.getString("d_name");
                int phone = rs.getInt("d_phoneNum");
                String address = rs.getString("d_address");
                String spe = rs.getString("d_sep");
                String dept = rs.getString("d_dept");
                String gender = rs.getString("d_gender");
                int age = rs.getInt("d_age");
                String join = rs.getString("d_join");
                String leave = rs.getString("d_leave");
                Object[] obj = {id, password, username, name, phone, address, spe, dept, gender, age, join, leave};
                DTM = (DefaultTableModel) doctor_table.getModel();
                DTM.addRow(obj);

            }

        } catch (SQLException ex) {
            Logger.getLogger(SearchForDoc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void search(String str) {
        DTM = (DefaultTableModel) doctor_table.getModel();
        TableRowSorter<DefaultTableModel> def = new TableRowSorter<>(DTM);
        doctor_table.setRowSorter(def);
        def.setRowFilter(RowFilter.regexFilter(str));
    }

