    private void btn_idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btn_idKeyReleased
       String id=btn_id.getText();
       try {
            PreparedStatement stmt=con.prepareStatement("select * from patient where p_id=?");
            stmt.setString(1, id);
            ResultSet rs=stmt.executeQuery();
           
            if(rs.next())
        {
            if(id.equals(rs.getString("p_id")))
            {
                btn_add.setText(rs.getString("p_address"));
                btn_city.setText(rs.getString("p_city"));
                btn_name.setText(rs.getString("p_f_name"));
                btn_name2.setText(rs.getString("p_m_name"));
                btn_name3.setText(rs.getString("p_l_name"));
                btn_phone.setText(rs.getString("p_phoneNum"));
                String malee=rs.getString("p_gender");
                if(btn_male.equals(rs.getString("p_gender"))){
                      btn_male.setSelected(true);
                }else
                     btn_female.setSelected(true);
                  
                   
               btn_age.setSelectedItem(rs.getString("p_age"));
               btn_d1.setSelectedItem(rs.getString("p_dateDay"));
               btn_d2.setSelectedItem(rs.getString("p_dateMonth"));
               btn_d3.setSelectedItem(rs.getString("p_dateYear"));
               btn_bloodtype.setSelectedItem(rs.getString("p_bloodType"));
            }
 
            }
        } catch (SQLException ex) {
            Logger.getLogger(NurseForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_idKeyReleased

