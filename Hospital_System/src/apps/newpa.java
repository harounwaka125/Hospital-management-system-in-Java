    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:

        if(!btn_name.getText().isEmpty()&& !btn_name2.getText().isEmpty()&&!btn_name3.getText().isEmpty())
        {
            try {
                String firstname=btn_name.getText();
                String middlename=btn_add.getText();
                String lastname=btn_add.getText();
                String gender;
                if(btn_male.isSelected())
                    gender="male";
                else
                    gender="female";
                String stat;
                if(btn_single.isSelected())
                    stat="Single";
                else
                    stat="Married";
                int age=Integer.parseInt((String)btn_age.getSelectedItem());
                int ph=Integer.parseInt(btn_phone.getText());
                String cit=btn_city.getText();
                String add=btn_add.getText();
                String date1=btn_d1.getSelectedItem().toString();
                String date2=btn_d2.getSelectedItem().toString();
                String date3=btn_d3.getSelectedItem().toString();
                String bloodtypes=btn_bloodtype.getSelectedItem().toString();
                PreparedStatement stmt=con.prepareStatement("insert into patient (p_f_name ,p_m_name ,p_l_name ,p_age ,p_gender,p_phoneNum ,p_address,p_city ,p_dateDay ,p_dateMonth ,p_dateYear ,p_bloodType ,p_MartialStatus) values (?,?,?,?,?,?,?,?,?,?,?,?,?) ");
                stmt.setString(1, firstname);
                stmt.setString(2, middlename);
                stmt.setString(3, lastname);
                stmt.setInt(4, age);
                stmt.setString(5, gender);
                stmt.setInt(6, ph);
               
                stmt.setString(7, add);
                stmt.setString(8, cit);
                stmt.setString(9, date1);
                stmt.setString(10, date2);
                stmt.setString(11, date3);
                stmt.setString(12, bloodtypes);
                stmt.setString(13, stat);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "save data success");
                btn_add.setText(" ");
                btn_name.setText("");
                btn_name2.setText("");
                btn_name3.setText("");
                btn_city.setText("");
                btn_phone.setText("");
                btn_bloodtype.setSelectedIndex(0);
                btn_d1.setSelectedIndex(0);
                btn_d2.setSelectedIndex(0);
                btn_d3.setSelectedIndex(0);
                btn_age.setSelectedIndex(0);
                buttonGroup1.clearSelection();
                buttonGroup2.clearSelection();
            } catch (SQLException ex) {
                Logger.getLogger(newpa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_saveActionPerformed

