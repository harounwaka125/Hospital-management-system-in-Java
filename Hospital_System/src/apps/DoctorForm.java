    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        if (btn_user.getText().equals("") || btn_name.getText().equals("") || btn_address.getText().equals("") || btn_spe.getText().isEmpty() || btn_phone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Make sure you enter all fields ");
        } else {
            try {

                String username = btn_user.getText();
                String password = btn_pass.getText();
                String doctorname = btn_name.getText();
                String specialzatoin = btn_spe.getText();
                String address = btn_address.getText();
                int phone = Integer.parseInt(btn_phone.getText());
                String department = (String) btn_dept.getSelectedItem();
                int year = Integer.parseInt((String) btn_age.getSelectedItem());
                String join = (String) btn_join.getSelectedItem();
                String end = (String) btn_leave.getSelectedItem();
                String gender;
                if (btn_male.isSelected()) {
                    gender = "male";
                } else {
                    gender = "female";
                }
                PreparedStatement stmt = con.prepareStatement("insert into doctor (d_username,d_password,d_name,d_phoneNum,d_address,d_sep,d_dept,d_gender,d_age,d_join,d_leave) values (?,?,?,?,?,?,?,?,?,?,?)");

                stmt.setString(1, username);
                stmt.setString(2, password);
                stmt.setString(3, doctorname);
                stmt.setInt(4, phone);
                stmt.setString(5, address);
                stmt.setString(6, specialzatoin);
                stmt.setString(7, department);
                stmt.setString(8, gender);
                stmt.setInt(9, year);
                stmt.setString(10, join);
                stmt.setString(11, end);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, ("save data success"));
                btn_user.setText("");
                btn_pass.setText("");
                btn_name.setText("");
                btn_phone.setText("");
                btn_address.setText("");
                btn_spe.setText("");
                buttonGroup1.clearSelection();
                btn_dept.setSelectedIndex(0);
                btn_age.setSelectedIndex(0);
                btn_join.setSelectedIndex(0);
                btn_leave.setSelectedIndex(0);
                btn_id.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(DoctorForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_saveButtonActionPerformed

