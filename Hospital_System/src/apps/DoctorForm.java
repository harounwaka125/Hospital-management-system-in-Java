    private void btn_idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btn_idKeyReleased
        try {
            String id = btn_id.getText();
            PreparedStatement stmt = con.prepareStatement("select * from doctor where doctor_id=?");
            stmt.setString(1, id);
            ResultSet rs = stmt.executeQuery();
            while (rs.next()) {
                if (id.equals(rs.getString("doctor_id"))) {
                    btn_address.setText(rs.getString("d_address"));
                    btn_age.setSelectedItem(rs.getString("d_age"));
                    btn_dept.setSelectedItem(rs.getString("d_dept"));
                    btn_join.setSelectedItem(rs.getString("d_join"));
                    btn_leave.setSelectedItem(rs.getString("d_leave"));
                    btn_name.setText(rs.getString("d_name"));
                    btn_pass.setText(rs.getString("d_password"));
                    btn_phone.setText(rs.getString("d_phoneNum"));
                    btn_user.setText(rs.getString("d_username"));
                    btn_spe.setText(rs.getString("d_sep"));
                    String st = rs.getString("d_gender");
                    if (st.equals("male")) {
                        btn_male.setSelected(true);
                    } else {
                        btn_female.setSelected(true);
                    }
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(DoctorForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_idKeyReleased

